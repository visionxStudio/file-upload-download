exports.postReset = (req, res, next) => {
  crypto.randomBytes(32, (err, buffer) => {
    if (err) {
      console.log(err);
      return res.redirect('/reset');
    }
    const token = buffer.toString('hex');
    User.findOne({
        email: req.body.email
      })
      .then(user => {
        if (!user) {
          req.flash('error', 'No account is associated with that email!');
          return res.redirect('/reset');
        }
        // if the user is found
        user.resetToken = token;
        user.resetTokenExpiration = Date.now() + 300000;
        return user.save();
      })
      .then(result => {
        res.redirect('/');
        const msg = {
          to: req.body.email,
          from: 'manish.rajak2055@gmail.com',
          subject: 'Password Reset',
          html: `
            <p>You requested a password reset</p>
            <p><strong>Click the link below to Reset your Password</strong></p>
            <p><a href="http://localhost:3000/reset/${token}">CLICK HERE!</p>
          `
        };
        return sgMail.send(msg)
          .then(success => {
            console.log('Reset Email sent successful!');
          })
          .catch(err => {
            throw new Error('Error while sending the reset email!')
          });
      })
      .catch(err => {
        console.log(err);
      });
  });
};

exports.getNewPassword = (req, res, next) => {
  const token = req.params.token;
  User.findOne({
      resetToken: token,
      resetTokenExpiration: {
        $gt: Date.now()
      }
    })
    .then(user => {
      let message = req.flash('error');
      if (message.length > 0) {
        message = message[0];
      } else {
        message = null;
      }
      res.render('auth/new-password', {
        path: '/new-password',
        pageTitle: 'New Password',
        errorMessage: message,
        userId: user._id.toString(),
        passwordToken: token
      });
    })
    .catch(err => {
      console.log(err);
    });
};

exports.postNewPassword = (req, res, next) => {
  const newPassword = req.body.password;
  const userId = req.body.userId;
  const passwordToken = req.body.passwordToken;
  let resetUser;

  User.findOne({
      resetToken: passwordToken,
      resetTokenExpiration: {
        $gt: Date.now()
      },
      _id: userId
    })
    .then(user => {
      resetUser = user;
      return bcrypt.hash(newPassword, 12);
    })
    .then(hashedPassword => {
      resetUser.password = hashedPassword;
      resetUser.resetToken = undefined;
      resetUser.resetTokenExpiration = undefined;
      return resetUser.save();
    })
    .then(result => {
      res.redirect('/login');
    })
    .catch(err => {
      console.log(err);
    });
};